import json

def retrieve_context(topic: str, collection, threshold=0.35) -> dict:
    cleaned = normalize_query(topic)
    if cleaned.lower() != topic.strip().lower():
        print(f'📝 Corrected: "{topic}" → "{cleaned}"')

    results = collection.query(
        query_texts=[cleaned], n_results=8,
        include=['documents', 'distances', 'metadatas']
    )
    docs  = results['documents'][0] if results['documents'] else []
    dists = results['distances'][0]  if results['distances']  else []
    metas = results['metadatas'][0]  if results['metadatas']  else []
    score = max(0.0, 1.0 - (dists[0] / 2.0)) if dists else 0.0

    if score >= threshold and docs:
        ctx = deduplicate_sentences('\n\n'.join(docs))
        pages = list({str(m.get('page','')) for m in metas})
        print(f'✅ Doc context retrieved (confidence: {score:.2f}) | Pages: {", ".join(sorted(pages))}')
        return {'cleaned_topic': cleaned, 'context': ctx,
                'source': 'doc', 'confidence': score, 'pages': pages}

    print(f'⚠️  Low doc confidence ({score:.2f}) — fetching from web...')
    web = fetch_web_context(cleaned)
    return {'cleaned_topic': cleaned, 'context': web,
            'source': 'web', 'confidence': score, 'pages': []}

