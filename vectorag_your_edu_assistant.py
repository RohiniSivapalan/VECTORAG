# Changes: added _wiki_full() for richer quiz context (full article not just summary)
import requests, urllib.parse
from bs4 import BeautifulSoup

def _wiki_summary(topic: str) -> str:
    try:
        enc  = urllib.parse.quote(topic.replace(' ', '_'))
        resp = requests.get(f'https://en.wikipedia.org/api/rest_v1/page/summary/{enc}',
                            timeout=8, headers={'User-Agent': 'VectorAG/3.0'})
        if resp.status_code == 200:
            e = resp.json().get('extract', '')
            if len(e) > 100: return e[:3000]
    except: pass
    return ''

def _wiki_full(topic: str) -> str:
    """Full Wikipedia article — gives more facts for quiz generation."""
    try:
        resp = requests.get('https://en.wikipedia.org/w/api.php',
            params={'action':'query','prop':'extracts','explaintext':True,
                    'titles':topic,'format':'json','exchars':6000},
            timeout=10, headers={'User-Agent': 'VectorAG/3.0'})
        if resp.status_code == 200:
            for p in resp.json().get('query',{}).get('pages',{}).values():
                txt = p.get('extract','')
                if len(txt) > 300: return txt[:5000]
    except: pass
    return ''

def _gfg(topic: str) -> str:
    try:
        r = requests.get(f'https://www.geeksforgeeks.org/?s={urllib.parse.quote(topic)}',
                         timeout=8, headers={'User-Agent': 'VectorAG/3.0'})
        soup = BeautifulSoup(r.text, 'html.parser')
        lt = soup.find('h2', class_='entry-title')
        if lt and lt.find('a'):
            ar = requests.get(lt.find('a')['href'], timeout=8, headers={'User-Agent': 'VectorAG/3.0'})
            t = ' '.join(p.get_text() for p in BeautifulSoup(ar.text,'html.parser').find_all('p')[:6])
            if len(t) > 200: return t[:2000]
    except: pass
    return ''

def fetch_web_context(topic: str) -> str:
    """For chat: summary is enough."""
    parts = []
    w = _wiki_summary(topic)
    if w: parts.append(f'[Wikipedia]\n{w}')
    if len(' '.join(parts)) < 500:
        g = _gfg(topic)
        if g: parts.append(f'[GeeksForGeeks]\n{g}')
    return '\n\n'.join(parts) if parts else ''

def fetch_web_quiz_context(topic: str) -> str:
    """For quiz: full article = more facts = better questions."""
    full = _wiki_full(topic)
    return full if full else fetch_web_context(topic)

