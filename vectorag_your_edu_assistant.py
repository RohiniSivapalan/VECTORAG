#   • Web quiz uses _wiki_full() for more content → more question variety
#   • Template fallback is now smart (3 question types) not dumb repeated questions
#   • LLM prompt tightened for faster token generation
import re, json

def _safe_json_parse(raw: str) -> list:
    for pat in [r'\[\s*\{.*?\}\s*\]', r'\[.*?\]']:
        m = re.search(pat, raw, re.DOTALL)
        if m:
            try: return json.loads(m.group(0))
            except:
                try: return json.loads(re.sub(r',\s*([}\]])', r'\1', m.group(0)))
                except: continue
    return []

def _validate_questions(raw_list: list) -> list:
    valid = []
    for q in raw_list:
        if not isinstance(q, dict): continue
        if not (isinstance(q.get('question'), str) and len(q['question']) > 10): continue
        if not (isinstance(q.get('options'), list) and len(q['options']) == 4): continue
        if not all(isinstance(o, str) and len(o) > 1 for o in q['options']): continue
        if not (isinstance(q.get('answer'), str) and q['answer'] in q['options']): continue
        q.setdefault('explanation', f"Correct answer: {q['answer']}")
        valid.append(q)
    return valid

def generate_mcq_quiz(raw_topic: str, collection, num_questions: int = 5) -> dict:
    """
    Full pipeline:
    1. Normalize + correct query
    2. Retrieve from ChromaDB (deduplicated)
    3. Auto web fallback with full Wikipedia article
    4. LLM generation if available, else smart template quiz
    5. JSON parsing + validation
    """
    result     = retrieve_context(raw_topic, collection)
    topic      = result['cleaned_topic']
    context    = result['context']
    source     = result['source']
    confidence = result['confidence']

    # Enrich with web when doc confidence is low
    if source in ('web', 'doc_low') or not context.strip():
        web = fetch_web_quiz_context(topic)
        if web:
            context = web if not context.strip() else context + '\n\n' + web
            if not context.strip(): source = 'web'

    if not context.strip():
        print(f'❌ No context found for "{topic}"')
        return {'questions': [], 'source': source, 'confidence': confidence}

    questions = []

    if llm:
        prompt = (
            f'[INST] <<SYS>>\n'
            f'Generate exactly {num_questions} MCQ questions from the context.\n'
            f'Rules: 4 options each, "answer" = exact correct option, add "explanation", no duplicates, JSON only.\n'
            f'<</SYS>>\n\n'
            f'Context:\n{context[:2800]}\n\n'
            f'[{{"question":"...","options":["A","B","C","D"],"answer":"A","explanation":"..."}}]\n'
            f'Generate {num_questions}: [/INST]'
        )
        out       = llm.create_chat_completion(
            messages=[{'role': 'user', 'content': prompt}],
            max_tokens=3500, temperature=0.05, repeat_penalty=1.15
        )
        questions = _validate_questions(_safe_json_parse(out['choices'][0]['message']['content']))
        print(f'✅ LLM: {len(questions)}/{num_questions} valid questions')

    if not questions:
        questions = generate_template_quiz(context, topic, num_questions)
        print(f'✅ Template: {len(questions)}/{num_questions} questions | source: {source.upper()}')

    return {
        'topic':      topic,
        'questions':  questions[:num_questions],
        'source':     source,
        'confidence': confidence,
    }

