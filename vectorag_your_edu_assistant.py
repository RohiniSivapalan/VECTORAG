# ── STEP 10: TEMPLATE QUIZ (no LLM needed, always works) ────────────────────
# Changes: 3 question types (definition, fill-blank, which-is-correct)
#          OCR noise filter on terms (_NOISE_TERMS)
#          Much better than original single-type fallback
import re, random

_NOISE_TERMS = {
    'gtu', 'marks', 'winter', 'summer', 'technical', 'publications',
    'fig', 'figure', 'table', 'note', 'page', 'unit', 'chapter',
}

def _get_defined_terms(text: str) -> list:
    """Strict term extractor — rejects OCR sentence fragments."""

    _SENTENCE_STARTERS = {
        'with','from','if','it','in','on','at','by','an','a','the','its','this',
        'that','these','those','when','where','which','who','what','how','since',
        'because','although','however','therefore','thus','hence','while','both',
        'each','some','most','all','any','for','as','but','so','yet','nor','or',
    }
    _GENERIC = {
        'internet connection','power consumption','data storage','market adaptability',
        'group collaboration','document access','software updates','computer costs',
    }
    raw = re.findall(r'([A-Z][a-zA-Z-]{2,20}(?:\s+[a-zA-Z-]{2,20}){0,3})\s*[:\-]\s*([^.\n]{20,150})', text)
    seen, result = set(), []
    for term, defn in raw:
        term, defn = term.strip(), defn.strip()
        tl, words = term.lower(), term.split()
        if not 1 <= len(words) <= 4: continue
        if words[0].lower() in _SENTENCE_STARTERS: continue
        if any(n in tl for n in _NOISE_TERMS): continue
        if tl in _GENERIC: continue
        if sum(c.isalpha() or c.isspace() for c in term) / len(term) < 0.92: continue
        if any(len(w) < 2 for w in words): continue
        if words[-1].lower() in {'the','a','an','of','in','on','at','to','and','or'}: continue
        if sum(c.isalpha() or c.isspace() for c in defn) / len(defn) < 0.60: continue
        if len(re.findall(r'[0-9/|¢£]', defn)) > 4: continue
        if len(defn.split()) < 4: continue
        if defn[0].islower() and defn.split()[0] not in {'a','an','the','it','its','this','that'}: continue
        if term.isupper() and len(term) > 5: continue
        if tl not in seen:
            seen.add(tl); result.append((term, defn[:120]))
    return result

def generate_template_quiz(context: str, topic: str, num_q: int) -> list:
    random.seed(42)
    sents = [s.strip() for s in re.split(r'(?<=[.!?])\s+', context)
             if 50 < len(s.strip()) < 300]
    terms = _get_defined_terms(context)
    questions = []

    # Type 1: Definition questions (highest quality)
    if len(terms) >= 4:
        defns = [d for _, d in terms]
        for term, defn in terms:
            if len(questions) >= num_q: break
            others = [d for d in defns if d != defn]
            random.shuffle(others)
            if len(others) < 3: continue
            opts = [defn] + others[:3]; random.shuffle(opts)
            questions.append({'question': f"What is '{term}'?",
                              'options': opts, 'answer': defn,
                              'explanation': f'{term}: {defn}'})

    # Type 2: Fill-in-the-blank
    for sent in sents:
        if len(questions) >= num_q: break
        words = sent.split()
        cands = [w.rstrip('.,;:') for w in words[2:] if len(w.rstrip('.,;:')) > 5 and w[0].isupper()]
        if not cands: cands = [w.rstrip('.,;:') for w in words[3:] if len(w.rstrip('.,;:')) > 6]
        if not cands: continue
        blank = cands[0]
        blanked = sent.replace(blank, '______', 1)
        if '______' not in blanked: continue
        pool = list({w for w in re.findall(r'\b[A-Za-z]{5,}\b', context) if w.lower() != blank.lower()})
        random.shuffle(pool)
        if len(pool) < 3: continue
        opts = [blank] + pool[:3]; random.shuffle(opts)
        questions.append({'question': f"Fill in the blank: '{blanked[:180]}'",
                          'options': opts, 'answer': blank,
                          'explanation': f"Complete sentence: '{sent[:200]}'"})

    # Type 3: Which statement is correct
    for sent in sents:
        if len(questions) >= num_q: break
        if any(sent[:50] in q.get('explanation','') for q in questions): continue
        others = [s[:140] for s in sents if s != sent and len(s) > 50]
        random.shuffle(others)
        if len(others) < 3: continue
        opts = [sent[:140]] + others[:3]; random.shuffle(opts)
        questions.append({'question': f"Which statement about '{topic}' is correct?",
                          'options': opts, 'answer': sent[:140],
                          'explanation': f"From document: '{sent[:200]}'"})

