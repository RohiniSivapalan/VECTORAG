_NOISE_LINE = re.compile(r'TECHNICAL PUBLICATIONS|an up-thrust|GTU\s*[:\.]', re.I)

def _clean_ocr(raw: str) -> str:
    lines = []
    for line in raw.splitlines():
        s = line.strip()
        if not s or len(s) < 4:
            continue
        if re.match(r'^\d+\s*[-]\s*\d+$', s):   # page numbers like "1 - 2"
            continue
        if _NOISE_LINE.search(s):                 # publisher footers
            continue
        alpha = sum(c.isalpha() or c.isspace() for c in s)
        if alpha / len(s) < 0.45:                 # mostly symbols/digits
            continue
        lines.append(s)
    return re.sub(r' {2,}', ' ', ' '.join(lines)).strip()

